"""
compare_search.py
=================
Compare Uniform Cost Search and A* across 10 randomised warehouse
configurations generated by WarehouseEnv.reset(randomize=True).

For each configuration:
  - Pickup (P) and dropoff (D) tiles are placed at random empty cells.
  - The robot starts at a random empty cell (distinct from P and D).

Both algorithms solve the same two-phase task:
  1. Navigate to the pickup tile and collect the item (PICK).
  2. Navigate to the dropoff tile and deliver the item (goal).

Output
------
  Console  : per-run log, optimality check, mean-statistics summary.
  Figure 1 : bar chart -- mean nodes expanded over 10 trials (UCS vs A*).
  Figure 2 : summary statistics table.
  Figure 3 : UCS animation -- grid + cumulative reward + distance graphs.
  Figure 4 : A* animation -- same graphs.
"""

from __future__ import annotations

from warehouse_env import WarehouseEnv
from ucs_pathfinder import uniform_cost_search, _initial_state, _get_frames
from astar_pathfinder import astar_search
from warehouse_viz import replay_animation

NUM_CONFIGS = 10


# ---------------------------------------------------------------------------
# Core comparison logic
# ---------------------------------------------------------------------------


def _run_config(env: WarehouseEnv, run: int) -> dict:
    """Run both searches on the current env state; return a result dict."""
    pickup_pos = env._find_tile("P")
    dropoff_pos = env._find_tile("D")
    start = env.start_pos

    ucs_path, ucs_stats = uniform_cost_search(env)
    astar_path, astar_stats = astar_search(env)

    ucs_solved = ucs_path is not None
    astar_solved = astar_path is not None
    lengths_match = (
        ucs_solved
        and astar_solved
        and ucs_stats["path_length"] == astar_stats["path_length"]
    )

    return {
        "run": run,
        "start": start,
        "pickup": pickup_pos,
        "dropoff": dropoff_pos,
        # UCS
        "ucs_nodes": ucs_stats["nodes_expanded"],
        "ucs_length": ucs_stats["path_length"],
        "ucs_frontier": ucs_stats["max_frontier_size"],
        "ucs_time": ucs_stats["computation_time"],
        "ucs_solved": ucs_solved,
        # A*
        "astar_nodes": astar_stats["nodes_expanded"],
        "astar_length": astar_stats["path_length"],
        "astar_frontier": astar_stats["max_frontier_size"],
        "astar_time": astar_stats["computation_time"],
        "astar_solved": astar_solved,
        # Optimality
        "optimal_match": lengths_match,
    }


# ---------------------------------------------------------------------------
# Metrics for animation
# ---------------------------------------------------------------------------


def _manhattan(a: tuple, b: tuple) -> int:
    return abs(a[0] - b[0]) + abs(a[1] - b[1])


def _compute_path_metrics(
    path: list,
    initial: tuple,
    pickup_pos: tuple,
    dropoff_pos: tuple,
) -> dict:
    """
    Build per-frame metric lists for replay_animation().

    rewards      : cumulative reward at each frame
                   (PICK earns +4.9, each move costs -0.1)
    dist_pickup  : Manhattan distance from robot to P at each frame
    dist_dropoff : Manhattan distance from robot to D at each frame
    """
    cumulative = 0.0
    rewards = []
    dist_pickup = []
    dist_dropoff = []

    r0, c0, _ = initial
    rewards.append(0.0)
    dist_pickup.append(_manhattan((r0, c0), pickup_pos))
    dist_dropoff.append(_manhattan((r0, c0), dropoff_pos))

    for action, state in path:
        r, c, _ = state
        cumulative += 4.9 if action == "PICK" else -0.1
        rewards.append(round(cumulative, 4))
        dist_pickup.append(_manhattan((r, c), pickup_pos))
        dist_dropoff.append(_manhattan((r, c), dropoff_pos))

    return {
        "rewards": rewards,
        "dist_pickup": dist_pickup,
        "dist_dropoff": dist_dropoff,
    }


