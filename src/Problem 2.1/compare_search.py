def compare(num_configs: int = NUM_CONFIGS) -> list[dict]:
    env = WarehouseEnv()

    print("=" * 72)
    print("  UCS vs A*  -  Warehouse Search Comparison")
    print("=" * 72)
    print("\nDefault warehouse layout (walls fixed; P/D randomised each trial):")
    _print_layout(env.grid)
    print(f"\nRunning {num_configs} random trials via reset(randomize=True)...\n")

    results = []
    for run in range(1, num_configs + 1):
        env.reset(randomize=True)
        env.start_pos = env.state.robot_pos  # sync randomised start
        results.append(_run_config(env, run))

    # Console output
    _print_run_log(results)
    _print_optimality_check(results)
    _print_summary(results)

    # Static visualisations
    print("\nGenerating visualisations...")
    _make_bar_chart(results)
    _make_summary_table(results)

    # Animations on the default (non-randomised) layout
    print("\nRunning UCS and A* on the default layout for animation...")
    demo_env = WarehouseEnv()

    ucs_path, _ = uniform_cost_search(demo_env)
    astar_path, _ = astar_search(demo_env)

    if ucs_path:
        _show_animation(demo_env, ucs_path, "UCS")
    else:
        print("UCS: no solution found on default layout.")

    if astar_path:
        _show_animation(demo_env, astar_path, "A*")
    else:
        print("A*: no solution found on default layout.")

    return results


if __name__ == "__main__":
    compare()
