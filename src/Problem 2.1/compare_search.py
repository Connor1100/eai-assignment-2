def _fmt(pos) -> str:
    return f"({pos[0]},{pos[1]})" if pos else "None"


def _print_layout(grid: list[str]) -> None:
    for line in grid:
        print("  " + line)


def _print_run_log(results: list[dict]) -> None:
    """Per-run table: path length, nodes expanded, frontier size, time."""
    w = dict(run=3, pos=7, nodes=7, length=6, front=7, ms=8, match=6)
    hdr = (
        f"{'Run':>{w['run']}}  "
        f"{'Start':<{w['pos']}}  {'Pickup':<{w['pos']}}  {'Dropoff':<{w['pos']}}  "
        f"{'UCS-N':>{w['nodes']}}  {'UCS-L':>{w['length']}}  "
        f"{'UCS-Fr':>{w['front']}}  {'UCS-ms':>{w['ms']}}  "
        f"{'A*-N':>{w['nodes']}}  {'A*-L':>{w['length']}}  "
        f"{'A*-Fr':>{w['front']}}  {'A*-ms':>{w['ms']}}  "
        f"{'Optim?':>{w['match']}}"
    )
    print(hdr)
    print("-" * len(hdr))

    for r in results:
        us, as_ = r["ucs_solved"], r["astar_solved"]

        def _v(key, solved):
            return str(r[key]) if solved else "FAIL"

        ucs_ms_s = f"{r['ucs_time'] * 1000:.3f}" if us else "FAIL"
        astar_ms_s = f"{r['astar_time'] * 1000:.3f}" if as_ else "FAIL"

        print(
            f"{r['run']:>{w['run']}}  "
            f"{_fmt(r['start']):<{w['pos']}}  "
            f"{_fmt(r['pickup']):<{w['pos']}}  "
            f"{_fmt(r['dropoff']):<{w['pos']}}  "
            f"{_v('ucs_nodes', us):>{w['nodes']}}  "
            f"{_v('ucs_length', us):>{w['length']}}  "
            f"{_v('ucs_frontier', us):>{w['front']}}  "
            f"{ucs_ms_s:>{w['ms']}}  "
            f"{_v('astar_nodes', as_):>{w['nodes']}}  "
            f"{_v('astar_length', as_):>{w['length']}}  "
            f"{_v('astar_frontier', as_):>{w['front']}}  "
            f"{astar_ms_s:>{w['ms']}}  "
            f"{'YES' if r['optimal_match'] else 'NO':>{w['match']}}"
        )


def _print_optimality_check(results: list[dict]) -> None:
    solved_both = [r for r in results if r["ucs_solved"] and r["astar_solved"]]
    failures = [r for r in solved_both if not r["optimal_match"]]
    print()
    print("Optimality check")
    print("-" * 42)
    print(f"  Configs solved by both : {len(solved_both)}/{len(results)}")
    if failures:
        print(f"  PATH LENGTH MISMATCH in {len(failures)} run(s):")
        for r in failures:
            print(f"    Run {r['run']}: UCS={r['ucs_length']}  A*={r['astar_length']}")
    else:
        print("  All path lengths identical -- both algorithms are optimal.")


def _print_summary(results: list[dict]) -> None:
    """Print mean statistics across all solved configurations."""
    solved = [r for r in results if r["ucs_solved"] and r["astar_solved"]]
    n = len(solved)
    print()
    print(f"Mean statistics  ({n}/{len(results)} configs solved by both):")
    print("-" * 62)
    if n == 0:
        print("  Nothing to aggregate.")
        return

    def avg(key):
        return sum(r[key] for r in solved) / n

    mu_ucs_n = avg("ucs_nodes")
    mu_as_n = avg("astar_nodes")
    mu_ucs_l = avg("ucs_length")
    mu_as_l = avg("astar_length")
    mu_ucs_fr = avg("ucs_frontier")
    mu_as_fr = avg("astar_frontier")
    mu_ucs_ms = avg("ucs_time") * 1000
    mu_as_ms = avg("astar_time") * 1000
    node_savings = (mu_ucs_n - mu_as_n) / mu_ucs_n * 100 if mu_ucs_n else 0.0

    print(f"  {'Metric':<22}  {'UCS':>10}  {'A*':>10}  {'A* saves':>10}")
    print(f"  {'-' * 22}  {'-' * 10}  {'-' * 10}  {'-' * 10}")
    print(
        f"  {'Nodes expanded':<22}  {mu_ucs_n:>10.1f}  {mu_as_n:>10.1f}  {node_savings:>9.1f}%"
    )
    print(f"  {'Path length':<22}  {mu_ucs_l:>10.1f}  {mu_as_l:>10.1f}  {'(same)':>10}")
    print(f"  {'Max frontier size':<22}  {mu_ucs_fr:>10.1f}  {mu_as_fr:>10.1f}")
    print(f"  {'Time (ms)':<22}  {mu_ucs_ms:>10.4f}  {mu_as_ms:>10.4f}")


