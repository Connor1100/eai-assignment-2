def _make_bar_chart(results: list[dict]) -> None:
    """
    Two-bar chart: mean nodes expanded (UCS vs A*).
    Individual trial values overlaid as scatter points.
    """
    try:
        import matplotlib.pyplot as plt
        import numpy as np
    except ImportError:
        print("matplotlib/numpy not available; skipping bar chart.")
        return

    solved = [r for r in results if r["ucs_solved"] and r["astar_solved"]]
    if not solved:
        return

    ucs_vals = [r["ucs_nodes"] for r in solved]
    astar_vals = [r["astar_nodes"] for r in solved]

    mean_ucs = sum(ucs_vals) / len(ucs_vals)
    mean_as = sum(astar_vals) / len(astar_vals)
    std_ucs = (sum((v - mean_ucs) ** 2 for v in ucs_vals) / len(ucs_vals)) ** 0.5
    std_as = (sum((v - mean_as) ** 2 for v in astar_vals) / len(astar_vals)) ** 0.5

    fig, ax = plt.subplots(figsize=(6, 5))
    x = np.array([0, 1])
    colors = ["#4C72B0", "#DD8452"]
    means = [mean_ucs, mean_as]
    stds = [std_ucs, std_as]

    bars = ax.bar(
        x,
        means,
        width=0.45,
        color=colors,
        edgecolor="white",
        yerr=stds,
        capsize=6,
        error_kw={"elinewidth": 1.5},
    )

    for bar, mu in zip(bars, means):
        ax.text(
            bar.get_x() + bar.get_width() / 2,
            bar.get_height() + max(stds) * 0.15,
            f"{mu:.1f}",
            ha="center",
            va="bottom",
            fontsize=11,
            fontweight="bold",
        )

    jitter = np.linspace(-0.12, 0.12, len(solved))
    for xi, vals in zip(x, [ucs_vals, astar_vals]):
        ax.scatter(
            xi + jitter,
            vals,
            color="black",
            s=28,
            zorder=5,
            alpha=0.7,
            label="Individual trial" if xi == x[0] else "",
        )

    ax.set_xticks(x)
    ax.set_xticklabels(["UCS", "A*"], fontsize=13)
    ax.set_ylabel("Nodes Expanded", fontsize=11)
    ax.set_title(
        f"Mean Nodes Expanded over {len(solved)} Trials\n"
        "(error bars = std dev,  dots = individual trials)",
        fontsize=11,
    )
    ax.legend(fontsize=9)
    ax.set_ylim(0, max(max(ucs_vals), max(astar_vals)) * 1.3)
    ax.grid(axis="y", alpha=0.3)
    fig.tight_layout()
    plt.show()


def _make_summary_table(results: list[dict]) -> None:
    """Summary statistics rendered as a matplotlib table."""
    try:
        import matplotlib.pyplot as plt
    except ImportError:
        print("matplotlib not available; skipping summary table.")
        return

    solved = [r for r in results if r["ucs_solved"] and r["astar_solved"]]
    n = len(solved)
    if n == 0:
        return

    def avg(key):
        return sum(r[key] for r in solved) / n

    def std(key):
        mu = avg(key)
        return (sum((r[key] - mu) ** 2 for r in solved) / n) ** 0.5

    metrics_def = [
        ("Nodes expanded", "ucs_nodes", "astar_nodes", 1.0),
        ("Path length", "ucs_length", "astar_length", 1.0),
        ("Max frontier size", "ucs_frontier", "astar_frontier", 1.0),
        ("Time (ms)", "ucs_time", "astar_time", 1000.0),
    ]

    cells = []
    for label, uk, ak, scale in metrics_def:
        ua, us_ = avg(uk) * scale, std(uk) * scale
        aa, as_ = avg(ak) * scale, std(ak) * scale
        diff = (
            "same"
            if label == "Path length"
            else (f"{(ua - aa) / ua * 100:+.1f}%" if ua else "n/a")
        )
        cells.append(
            [label, f"{ua:.2f} +/- {us_:.2f}", f"{aa:.2f} +/- {as_:.2f}", diff]
        )

    col_labels = ["Metric", "UCS  (mean +/- std)", "A*  (mean +/- std)", "A* vs UCS"]

    fig, ax = plt.subplots(figsize=(10, 2.6))
    ax.set_axis_off()
    tbl = ax.table(cellText=cells, colLabels=col_labels, cellLoc="center", loc="center")
    tbl.auto_set_font_size(False)
    tbl.set_fontsize(10)

    for j in range(len(col_labels)):
        tbl[0, j].set_facecolor("#2C3E50")
        tbl[0, j].set_text_props(color="white", fontweight="bold")
    for i in range(1, len(cells) + 1):
        bg = "#EAF2FB" if i % 2 == 0 else "#FDFEFE"
        for j in range(len(col_labels)):
            tbl[i, j].set_facecolor(bg)

    tbl.auto_set_column_width(list(range(len(col_labels))))
    all_match = all(r["optimal_match"] for r in solved)
    note = (
        "Optimality: all path lengths agree."
        if all_match
        else "WARNING: path length mismatch!"
    )
    fig.suptitle(
        f"UCS vs A*: Summary Statistics  ({n}/{len(results)} trials solved)",
        fontsize=12,
        fontweight="bold",
        y=0.98,
    )
    fig.text(
        0.5, 0.01, note, ha="center", fontsize=9, color="green" if all_match else "red"
    )
    fig.tight_layout(rect=[0, 0.05, 1, 0.93])
    plt.show()


def _show_animation(env: WarehouseEnv, path: list, algo_name: str) -> None:
    """Show an animated solution with cumulative-reward and distance graphs."""
    initial = _initial_state(env)
    frames = _get_frames(env, path, initial)
    pickup_pos = env._find_tile("P")
    dropoff_pos = env._find_tile("D")
    metrics = _compute_path_metrics(path, initial, pickup_pos, dropoff_pos)

    print(
        f"\n{algo_name} animation  ({len(path)} steps)  -- close window to continue..."
    )
    replay_animation(frames, metrics=metrics, interval_ms=300)


